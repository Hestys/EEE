    public boolean girisleriDogrula() {
        String kuadi = kadi.getText();
        String isim = adi.getText();
        String soyd = soyadi.getText();
        String epst = eposta.getText();
        String cep = telefonNo.getText();
        String sfre1 = String.valueOf(sfre.getPassword());
        String sfree2 = String.valueOf(sfre2.getPassword());
        String dgm = dgmtarihi.getText();
        String ill = il.getSelectedItem().toString();
        String ilcee = ilce.getSelectedItem().toString();
        String adrs = adresText.getText();

        if (kuadi.trim().equals("") || isim.trim().equals("") || soyd.trim().equals("")
                || epst.trim().equals("") || cep.trim().equals("") || sfre1.trim().equals("")
                || sfree2.trim().equals("") || dgm.trim().equals("") || ill.trim().equals("") || ilcee.trim().equals("") || adrs.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Lütfen Bilgilerinizi Kontrol Edin!", "Boş Geçmeyiniz!", 2);
            return false;
        } else if (!sfre1.equals(sfree2)) {
            JOptionPane.showMessageDialog(null, "Şifreleriniz Uyuşmuyor!", "Lütfen Kontrol Edin!", 2);
            return false;
        } else {

            return true;
        }
    }
