    public boolean kadiBak(String kuadi) {
        PreparedStatement st;
        ResultSet rs;
        boolean kuadi_exit = false;

        String guery = "SELECT * FROM `kullanicilar` WHERE `kadi` = ?";

        try {
            st = mysqL.getConnection().prepareStatement(guery);
            st.setString(1, kuadi);
            rs = st.executeQuery();

            if (rs.next()) {
                kuadi_exit = true;
                JOptionPane.showMessageDialog(null, "Bu kullanıcı önceden kayıtlı", "Farklı bir kullanıcı ismi deneyin", 2);

            }

        } catch (SQLException ex) {
            Logger.getLogger(kayitOlFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        return kuadi_exit;
    }
//postanın benzersiz olduguna bakıldı

    public boolean epostaBak(String eposta) {
        PreparedStatement st;
        ResultSet rs;
        boolean eposta_exit = false;

        String guery = "SELECT * FROM `kullanicilar` WHERE `eposta` = ?";

        try {
            st = mysqL.getConnection().prepareStatement(guery);
            st.setString(1, eposta);
            rs = st.executeQuery();

            if (rs.next()) {
                eposta_exit = true;
                JOptionPane.showMessageDialog(null, "Farklı bir e-posta adresi deneyin", "Kayıtlı E-Posta", 2);

            }

        } catch (SQLException ex) {
            Logger.getLogger(kayitOlFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        return eposta_exit;
    }

