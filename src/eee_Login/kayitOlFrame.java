    private void kayitOlMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_kayitOlMouseClicked
        String kuadi = kadi.getText();
        String isim = adi.getText();
        String soyd = soyadi.getText();
        String epst = eposta.getText();
        String cep = telefonNo.getText();
        String sfre1 = String.valueOf(sfre.getPassword());
        String sfree2 = String.valueOf(sfre2.getPassword());
        String dgm = dgmtarihi.getText();
        String ill = il.getSelectedItem().toString();
        String ilcee = ilce.getSelectedItem().toString();
        String adrs = adresText.getText();

        if (girisleriDogrula()) {
            if (!kadiBak(kuadi)) {
                if (!epostaBak(epst)) {
                    PreparedStatement ps;
                    ResultSet re;
                    String kullaniciKayitQery = "INSERT INTO `kullanicilar`(`kadi`, `adi`, `soyadi`, `eposta`, `telefonno`, `sifre`, `dgmtarihi`,`il`,`ilce`, `adres`) VALUES (?,?,?,?,?,?,?,?,?,?)";

                    try {
                        ps = mysqL.getConnection().prepareStatement(kullaniciKayitQery);

                        ps.setString(1, kuadi);
                        ps.setString(2, isim);
                        ps.setString(3, soyd);
                        ps.setString(4, epst);
                        ps.setString(5, cep);
                        ps.setString(6, sfre1);
                        ps.setString(7, dgm);
                        ps.setString(8, ill);
                        ps.setString(9, ilcee);
                        ps.setString(10, adrs);

                        try {
                            if (ps.executeUpdate() != 0) {

                                JOptionPane.showMessageDialog(null, "Hesabınız oluşturuldu");
                            } else {
                                JOptionPane.showMessageDialog(null, "Hata: Bilgilerinizi Kontrol Edin!");
                            }

                        } catch (SQLException ex) {
                            Logger.getLogger(kayitOlFrame.class.getName()).log(Level.SEVERE, null, ex);
                        }

                    } catch (SQLException ex) {
                        Logger.getLogger(kayitOlFrame.class.getName()).log(Level.SEVERE, null, ex);
                    }

                }
            }

        }

    }//GEN-LAST:event_kayitOlMouseClicked

